/*
 * 자료구조 (Data Structure) 란?
 * - 데이터를 저장하고 표현하는 방법을 의미한다. (+ 즉, 자료구조는 데이터를 저장하고 저장 된
 * 데이터와 연관 된 연산을 모두 통칭한 용어라는 것을 알 수 있다.)
 * 
 * 자료구조 종류
 * - 단순 자료구조 (Primitive Data Structure)
 * - 복합 자료구조 (Non Primitive Data Structure)
 * 
 * 위와 같이 자료구조는 크게 2 가지 종류가 존재하며 일반적으로 자료구조 학문에서 다루는 종류는
 * 복합 자료구조를 다룬다. (+ 즉, 여러 데이터를 좀 더 효율적으로 처리하는 방법을 다룬다는 것을
 * 알 수 있다.)
 * 
 * 복합 자료구조는 내부적으로 데이터를 제어하는 형태에 따라 선형 자료구조와 비선형 자료구조로
 * 구분된다.
 * 
 * 선형 자료구조 (Linear Data Structure) 종류
 * - 리스트 (List)
 * - 스택 (Stack)
 * - 큐 (Queue)
 * - 덱 (Dequeue)
 * 
 * 비선형 자료구조 (Non Linear Data Structure) 종류
 * - 트리 (Tree)
 * - 해시 테이블 (Hash Table)
 * - 그래프 (Graph)
 * 
 * 위와 같이 자료구조에는 여러 종류가 존재하며 자료구조마다 데이터를 관리하는 방식이 다르기 때문에
 * 적절한 자료구조를 선택 및 활용하는 선구안이 필요하다. (+ 즉, 선택한 자료구조에 따라 작성하는 
 * 알고리즘이 달라진다는 것을 알 수 있다.)
 */
